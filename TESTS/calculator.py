def soma(x,y):
    soma=float(x+y)
    print(f'A soma é {soma}')

def subtração(x,y):
    subtração=float(x-y)
    print(f'A Subtração é {subtração}')

def multiplicação(x,y):
    multiplicação=float(x*y)
    print(f'A multiplicação é {multiplicação}')

def divisão(x,y):
    divisão=float(x/y)
    print(f'A divisão é {divisão}')
